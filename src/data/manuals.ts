// Функція для формування контексту з мануалів
export function buildManualContext(): string {
  return MANUALS.map((manual) => {
    const sections = manual.sections
      .map((s) => `### ${s.title}\n${s.content}`)
      .join("\n\n");
    return `## ${manual.carrier}\n${sections}`;
  }).join("\n\n---\n\n");
}
